# Variables
amount_of_apples = 2
cost_of_apples = 5
total_cost = amount_of_apples * cost_of_apples
print(total_cost)

# Variable name rules
# 1. Must start with a letter or underscore
# 2. Can only contain letters, numbers, and underscores
# 3. Case-sensitive
# 4. Avoid using Python keywords and function names
# 5. Use descriptive names
# 6. Use camelCase or underscores

# Variables can be reassigned to different values
cost_of_apples = cost_of_apples + 10
total_cost = amount_of_apples * cost_of_apples
print(total_cost)

# Shortcut Operators
# +=, -=, *=, /=, **=, %=
cost_of_apples += 2
total_cost = amount_of_apples * cost_of_apples
print(total_cost)
